<template>
    <button 
      @click="toggleTheme" 
      class="p-2 rounded-full hover:bg-gray-200 dark:hover:bg-dark-bg-secondary focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-primary-500"
      :aria-label="darkMode ? 'Beralih ke Light Mode' : 'Beralih ke Dark Mode'"
    >
      <!-- Sun icon for dark mode (to switch to light) -->
      <svg 
        v-if="darkMode" 
        class="h-5 w-5 text-yellow-400" 
        xmlns="http://www.w3.org/2000/svg" 
        fill="none" 
        viewBox="0 0 24 24" 
        stroke="currentColor"
      >
        <path 
          stroke-linecap="round" 
          stroke-linejoin="round" 
          stroke-width="2" 
          d="M12 3v1m0 16v1m9-9h-1M4 12H3m15.364 6.364l-.707-.707M6.343 6.343l-.707-.707m12.728 0l-.707.707M6.343 17.657l-.707.707M16 12a4 4 0 11-8 0 4 4 0 018 0z" 
        />
      </svg>
      <!-- Moon icon for light mode (to switch to dark) -->
      <svg 
        v-else 
        class="h-5 w-5 text-gray-600" 
        xmlns="http://www.w3.org/2000/svg" 
        fill="none" 
        viewBox="0 0 24 24" 
        stroke="currentColor"
      >
        <path 
          stroke-linecap="round" 
          stroke-linejoin="round" 
          stroke-width="2" 
          d="M20.354 15.354A9 9 0 018.646 3.646 9.003 9.003 0 0012 21a9.003 9.003 0 008.354-5.646z" 
        />
      </svg>
    </button>
</template>

<script setup lang="ts">
  import { computed } from 'vue';
  import { useThemeStore } from '~/stores/theme.store';
  
  const themeStore = useThemeStore();
  
  // Computed property untuk mendapatkan status dark mode
  const darkMode = computed(() => themeStore.darkMode);
  
  // Method untuk toggle theme
  const toggleTheme = () => {
    themeStore.toggleDarkMode();
  };
</script>